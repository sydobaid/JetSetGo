// Function to build formatted airport string
function buildAirportString(airport) {
    return `${airport.cityName}, ${airport.countryName}, (${airport.cityCode}), ${airport.airportName}`;
}

// Function to check if a string contains 'Airport'
function containsAirport(str) {
    return str.includes("Airport");
}

